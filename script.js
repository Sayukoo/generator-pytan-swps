    const numA = document.getElementById('numA');
    const numB = document.getElementById('numB');
    const qA = document.getElementById('qA');
    const qB = document.getElementById('qB');
    const drawBtn = document.getElementById('drawBtn');
    const resetBtn = document.getElementById('resetBtn');
    const helpDialog = document.getElementById('helpDialog');
    document.getElementById('helpBtn').onclick = () => helpDialog.showModal();
    document.getElementById('closeHelp').onclick = () => helpDialog.close();

    const cardEls = document.querySelectorAll('.card');
    const numEls = [numA, numB];

    function randInt(min, max) {
      const range = max - min + 1;
      const maxUint = 0xFFFFFFFF;
      const limit = Math.floor(maxUint / range) * range;
      const buf = new Uint32Array(1);
      let x;
      do {
        crypto.getRandomValues(buf);
        x = buf[0];
      } while (x >= limit);
      return min + (x % range);
    }

    function draw() {
      const N = QUESTIONS.length;
      let a = randInt(1, N);
      let b;
      do {
        b = randInt(1, N);
      } while (b === a);
      cardEls.forEach((c) => {
        c.classList.remove('pop', 'glow');
        void c.offsetWidth;
        c.classList.add('pop', 'glow');
      });
      numEls.forEach((n) => {
        n.classList.remove('flip');
        void n.offsetWidth;
        n.classList.add('flip');
      });
      numA.textContent = a;
      numB.textContent = b;
      qA.textContent = QUESTIONS[a - 1];
      qB.textContent = QUESTIONS[b - 1];
    }

    function reset() {
      numA.textContent = '–';
      numB.textContent = '–';
      qA.textContent = '';
      qB.textContent = '';
    }

    drawBtn.onclick = draw;
    resetBtn.onclick = reset;
    document.addEventListener('keydown', (e) => {
      if (e.code === 'Space') {
        e.preventDefault();
        draw();
      }
      if (e.key && e.key.toLowerCase() === 'r') {
        reset();
      }
    });